package ch02.item16;

public class Point01 {
    private double x;
    private double y;

    public Point01(double x,double y){
        this.x=x;
        this.y=y;
    }

    public double getX(){return x;}
    public double gety(){return y;}

    public void setX(double x){this.x=x;}
    public void sety(double y){this.y=y;}
}
